# Monte um algoritmo que leia um número e mostre o seu dobro triplo e raiz quadrada

# Pede um número ao usuário:
numero = int(input('Digite um numero inteiro: '))

# Exibe a resposta ao usuário
print(f'O dobro do número digitado é: {numero*2} \n'
      f'O triplo do número digitado é: {numero*3} \n'
      f'A raiz quadrada desse número é: {numero**0.5:.3f}')
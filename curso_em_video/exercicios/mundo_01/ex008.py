# Escreva um programa que leia um valor em metros e o exiba convertido em centimetros e milimetros

# Pede o valor em metro ao usuário
valor = float(input('Digite um valor em metros: '))

# Realiza as conversões e as exibe ao usuário
print(f'O valor digitado é igual a {valor*100}) centimetros, que é igual a {valor*1000} milimetros')
# Tuplas

# Cria uma tupla que representa um combo de um lanche
lanche = ('Hambúrguer', 'Batata frita', 'Refrigerante', 'Milk Shake')

# Exibe todos os elementos do lanche
print(lanche)

# Mostra o elemento presente no índice 1
print(lanche[1])

# Mostra os elementos do índice 1 ao 4
print(lanche[1:4])

# Mostra o último elemento da tupla
print(lanche[-1])

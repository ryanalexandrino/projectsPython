# Estrutura de Repetição
# Estrutura padrão do laço for

# Laço de repetição for padrão
for c in range(1 , 6):
    print(f'{c} - Olá!!')
print('Fim! \n')

# Laço de repetição for com iteração descrescente
for c in range(10 , 0, -1):
    print(f'{c} - Olá!!')
print('Fim! \n')

# Laço de repetição for com iteração de 2 em 2
for c in range(0, 20, 2):
    print(f'{c} - Olá!!')
print('Fim! \n')
